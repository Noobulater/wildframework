local function generate()
	for key, ent in pairs(ents.FindByModel("models/props/cs_militia/footlocker01_closed.mdl")) do
		function ent:useFunc(ply, entity) 
			sound.Play( "items/ammocrate_close.wav", ent:GetPos() + Vector(0,0,30), 100, 100, 1 )
			ent:SetModel("models/props/cs_militia/footlocker01_open.mdl")
			ent:SetSolid(0)
		end
	end

	for key, door in pairs(ents.FindByClass("prop_door_rotating")) do
		if IsValid(door) then
			door.isCade = true
			door:SetHealth(200)
			function door.breakFunc(dmginfo)
				local pos,ang,skin,model = door:GetPos(), door:GetAngles(), door:GetSkin(), door:GetModel()
				door:Remove()

				local ent = ents.Create("prop_physics")
				ent:SetPos(pos)
				ent:SetAngles(ang)
				ent:SetSkin(skin)
				ent:SetModel(model)
				ent:Spawn()

				ent:SetCollisionGroup(COLLISION_GROUP_DEBRIS)
				ent.isCade = false
				ent:GetPhysicsObject():EnableMotion(true)
				ent:GetPhysicsObject():Wake()
				if ent:GetPhysicsObject():IsDragEnabled() then
					ent:GetPhysicsObject():SetDragCoefficient(0.2)
				end
				ent:GetPhysicsObject():ApplyForceCenter(dmginfo:GetDamageForce())

			end
		end
	end
	for key, door in pairs(ents.FindByClass("func_movelinear")) do
		if IsValid(door) then
			door.isCade = true
			door:SetHealth(1000)
			function door.breakFunc( dmginfo )
				door:Fire("Open", '', 0)
				door.dontTarget = true
				timer.Simple(1, function() if IsValid(door) then door.dontTarget = false end end)
			end
		end
	end

	for key, brush in pairs(ents.FindByClass("func_brush")) do
		brush.dontTarget = true
	end

	for key, door in pairs(ents.FindByClass("func_door_rotating")) do
		if IsValid(door) then
			door.isCade = true
			function door.breakFunc( dmginfo )
				door:Fire("Open", '', 0)
				door.dontTarget = true
				timer.Simple(1, function() if IsValid(door) then door.dontTarget = false end end)
			end
		end
	end

